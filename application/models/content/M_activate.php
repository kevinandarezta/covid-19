	public function data(){
		$data = array();
		$data = session_check();
		//level_check($data['sLevel'],'1',1);

		$data = $this->start();
		$action = string(urinext('data'));
		$id = string(urinext('id'));

		$data['ACTION'] = 'data';

		$ptitle = array('');

		if(!empty($action) && !empty($id)){
			update("user","flag_aktif='1'","WHERE id_user='$id'");
	
			$body = '
			<div class="alert alert-success" role="alert">
				<strong>Akun anda telah di aktivasi, silahkan <a href="'.base_url().'pages/login">login</a></strong>
